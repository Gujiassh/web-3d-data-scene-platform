import { mkdir, readFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

import { writeGeneratedFile } from "./write-generated-file.mjs";

const outputDirectory = fileURLToPath(new URL("../src/generated/", import.meta.url));
const declaration = `// Generated by scripts/generate-validators.mjs. Do not edit.
interface ValidationError {
  readonly instancePath: string;
  readonly keyword: string;
  readonly params: Readonly<Record<string, unknown>>;
}

declare const validate: ((data: unknown) => boolean) & {
  errors: readonly ValidationError[] | null;
};

export default validate;
`;

await mkdir(outputDirectory, { recursive: true });
await Promise.all(
  [
    {
      schema: "scene-document.schema.json",
      output: "scene-document.validator",
    },
    {
      schema: "scene-document-1.0.schema.json",
      output: "scene-document-1.0.validator",
    },
    {
      schema: "scene-document-1.1.schema.json",
      output: "scene-document-1.1.validator",
    },
    {
      schema: "scene-document-1.2.schema.json",
      output: "scene-document-1.2.validator",
    },
    {
      schema: "scene-document-1.3.schema.json",
      output: "scene-document-1.3.validator",
    },
  ].flatMap(({ schema, output }) => [
    generateValidator(schema, output),
    writeGeneratedFile(
      fileURLToPath(new URL(`../src/generated/${output}.d.ts`, import.meta.url)),
      declaration,
    ),
  ]),
);

async function generateValidator(schemaFile, output) {
  const schemaPath = fileURLToPath(
    new URL(`../../../specs/001-product-foundation/contracts/${schemaFile}`, import.meta.url),
  );
  const schema = JSON.parse(await readFile(schemaPath, "utf8"));
  const ajv = new Ajv2020({
    allErrors: true,
    code: { esm: true, source: true },
    strict: true,
    strictRequired: false,
  });
  const validate = ajv.compile(schema);
  const standalone = standaloneCode(ajv, validate);
  const runtimeImports = [];
  const esm = standalone.replace(
    /const (\w+) = require\("([^"]+)"\)\.default;/gu,
    (_match, identifier, modulePath) => {
      runtimeImports.push(
        `import ${identifier}Module from "${modulePath}.js";`,
        `const ${identifier} = typeof ${identifier}Module === "function" ? ${identifier}Module : ${identifier}Module.default;`,
      );
      return "";
    },
  );
  const generated = [
    "// Generated by scripts/generate-validators.mjs. Do not edit.",
    ...runtimeImports,
    esm,
  ].join("\n");
  await writeGeneratedFile(
    fileURLToPath(new URL(`../src/generated/${output}.js`, import.meta.url)),
    generated,
  );
}
